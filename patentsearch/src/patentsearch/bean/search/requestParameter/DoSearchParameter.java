package patentsearch.bean.search.requestParameter;

public class DoSearchParameter  {
	/* 用户id*/
	private String uID;
	private String DbType;
	private String sID;
	private String searchFormula;
	public DoSearchParameter() {}
	public DoSearchParameter(String uID, String dbType, String sID,
			String searchFormula) {
		super();
		this.uID = uID;
		DbType = dbType;
		this.sID = sID;
		this.searchFormula = searchFormula;
	}
	public String getuID() {
		return uID;
	}
	public void setuID(String uID) {
		this.uID = uID;
	}
	public String getDbType() {
		return DbType;
	}
	public void setDbType(String dbType) {
		DbType = dbType;
	}
	public String getsID() {
		return sID;
	}
	public void setsID(String sID) {
		this.sID = sID;
	}
	public String getSearchFormula() {
		return searchFormula;
	}
	public void setSearchFormula(String searchFormula) {
		this.searchFormula = searchFormula;
	}
	

}
